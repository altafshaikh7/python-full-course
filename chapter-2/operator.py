
# 1.Arithmetic Operators
a = 10
b = 5
c = a + b # addition
d = a - b # subtraction
e = a * b # multiplication
f = a / b # division
print("Addition: " + str(c))
print("Subtraction: " + str(d))
print("Multiplication: " + str(e))
print("Division: " + str(f))


# 2.Assignment Operators
x = 10
x += 5 # x = x + 5
print("After += operator: " + str(x))  #x is increased by 5
x -= 3 # x = x - 3
print("After -= operator: " + str(x))  #x is decreased by 3
x *= 2 # x = x * 2
print("After *= operator: " + str(x))  #x is multiplied by 2
x /= 4 # x = x / 4
print("After /= operator: " + str(x))  #x is divided by 4

# 3.Comparison Operators
p = 10
q = 5
print("p > q: " + str(p > q))  # True
print("p < q: " + str(p < q))  # False
print("p == q: " + str(p == q))  # False
print("p != q: " + str(p != q))  # True
# 4.Logical Operators
r = True
s = False
print("true and false: " + str(r and s))  # False
print("true or false: " + str(r or s))  # True
print("not true: " + str(not r))  # False
print("not false: " + str(not s))  # True




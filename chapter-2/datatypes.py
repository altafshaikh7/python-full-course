

# Data data types in Python :

a = 10 # a is a variable and 10 is an integer data type
b = 3.14 # b is a variable and 3.14 is a float data type
c = "Hello, World!" # c is a variable and "Hello, World!" is a string data type
d = True # d is a variable and True is a boolean data type
e = None # e is a variable and None is a special data type representing the absence of a value
